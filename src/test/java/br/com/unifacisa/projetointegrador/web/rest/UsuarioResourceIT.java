    public static Usuario createEntity() {
        return new Usuario().login(DEFAULT_LOGIN).senhaHash(DEFAULT_SENHA_HASH).ativo(DEFAULT_ATIVO).ultimoAcesso(DEFAULT_ULTIMO_ACESSO);
    }

